    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        payment.setPaymentID(Integer.parseInt(paymentID.getText()));
        payment.setMemberID(Integer.parseInt(memberID.getText()));
        payment.setAmount((Double) amtPayed.getValue());
        payment.setDescription(descriptionField.getText());
        payment.setDate_payed(datePayedField.getText());
        payment.setDate_recorded(dateRecordedField.getText());
        
        //System.out.println(payment);
        //aDAO.savePayment(payment);
        if(validHelp.isObjectValid(payment)){
            aDAO.savePayment(payment);
            this.dispose();
        }
        //this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

