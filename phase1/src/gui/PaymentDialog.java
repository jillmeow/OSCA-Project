    public PaymentDialog(Window parent, boolean modal, MemberDAO dao, int id) {
        super(parent);
        initComponents();
        memberID.setText(Integer.toString(id));
        memberID.setEditable(false);
        validHelp.addTypeFormatter(amtPayed, "#0.00", Double.class);
        int length = 7;
        Random random = new Random();
        char[] digits = new char[length];
        digits[0] = (char) (random.nextInt(9) + '1');
        for (int i = 1; i < length; i++) {
            digits[i] = (char) (random.nextInt(10) + '0');
        }
        amtPayed.setValue(Double.parseDouble("0"));
        aDAO = dao;
        paymentID.setText(new String(digits));
        paymentID.setEditable(false);
        
    }

